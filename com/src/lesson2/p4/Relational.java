package lesson2.p4;

public class Relational {
    public static void main(String[] args) {
        int a = 4;
        int b = 1;
        boolean c = a < b;  //c = false
        c = a > b;  //true

        c = a == b;  //false
        c = a != b;  //true
        c = a >= b; //true
        c = a <= b; //false
    }

}
